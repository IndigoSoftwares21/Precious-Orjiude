const openMenu = ()=>{
    var navmenu = document.getElementById("mobilenav");
    navmenu.style.opacity = "1";
    navmenu.style.pointerEvents = "initial";

}
const closeMenu = ()=>{
    var navmenu = document.getElementById("mobilenav");
    navmenu.style.opacity = "0";
    navmenu.style.pointerEvents = "none";

}
